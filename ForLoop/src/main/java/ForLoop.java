
public class ForLoop {

	public static void main(String[] args) {
		int i,j;
		for(i=0;i<=5;i++)
		{
			for(j=0;j<=i;j++){
					System.out.print(" " +j); //we are printing the j value so it compares wit
			}
		
		System.out.println("");  // just moves into next line since the value is <=5 it moves 6 times
			}
			
		}

}
